class Solution {
  public:
    string makeFancyString(string s) {
        string res;
        for (char c : s) {
            int sz = res.size();
            if (sz >= 2 && res[sz - 1] == c && res[sz - 2] == c)
                continue;
            res.push_back(c);
        }
        return res;
    }
};

