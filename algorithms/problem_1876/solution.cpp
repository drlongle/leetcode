using namespace std;

class Solution {
public:
    int countGoodSubstrings(string s) {
        int res = 0, sz = s.size();
        for (int i = 2; i < sz; ++i) {
            if (s[i] != s[i-1] && s[i] != s[i-2] && s[i-1] != s[i-2])
                ++res;
        }

        return res;
    }
};

