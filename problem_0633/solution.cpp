class Solution {
public:
    bool judgeSquareSum(int c) {
        int limit = sqrt(c);
        for (long long i = 0; i <= limit; ++i) {
            int j = c - i*i;
            int k = sqrt(j);
            if (k*k == j)
                return true;
        }
        return false;
    }
};

