class RLEIterator {
public:
    RLEIterator(vector<int>& A): encoded(A), count(0), pointer(0), sz(A.size())
    {

    }
    
    int next(int n) {
        while (n > 0) {
            while (count == 0) {
                if (pointer >= sz)
                    return -1;
                count = encoded[pointer++];
                num = encoded[pointer++];
            }
            int subs = min(n, count);
            n -= subs;
            count -= subs;
        }

        return num;
    }
    vector<int> encoded;
    int num, count;
    size_t pointer, sz;
};


