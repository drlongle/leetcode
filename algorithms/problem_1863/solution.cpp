class Solution {
public:
    int visit(vector<int>& nums, size_t index, int curr) {
        if (index >= nums.size())
            return curr;
        int a = visit(nums, index+1, curr);
        int b = visit(nums, index+1, curr ^ nums[index]);
        return a + b;
    }
    int subsetXORSum(vector<int>& nums) {
        return visit(nums, 0, 0);
    }
};

