struct TreeNode {
    int val;
    TreeNode *left;
    TreeNode *right;
    TreeNode(int x) : val(x), left(NULL), right(NULL) {}
};

class Solution {
public:
    int res;

    int get_height(TreeNode* node) {
        if (node == nullptr)
            return 0;

        int hl = get_height(node->left);
        int hr = get_height(node->right);
        res = max(res, hl + hr);

        return 1 + max(hr, hl);;
    }
    
    int diameterOfBinaryTree(TreeNode* root) {
        res = 0;
        get_height(root);
        return res;
    }
};

