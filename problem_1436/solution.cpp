#define ll long long
#define ull unsigned long long

class Solution {
public:
    string destCity(vector<vector<string>>& paths) {
        unordered_map<string, bool> cities;
        for (auto& path: paths) {
            cities[path[0]] = false;
            cities.emplace(path[1], true);
        }
        auto it = find_if(begin(cities), end(cities), [] (auto& p) {return p.second;});
        return it->first;
    }
};

int main() {
    Solution sol;
    vector<vector<string>> paths;

    paths = {{"pYyNGfBYbm","wxAscRuzOl"},{"kzwEQHfwce","pYyNGfBYbm"}};
    
    cout << sol.destCity(paths) << endl;
    return 0;
}
