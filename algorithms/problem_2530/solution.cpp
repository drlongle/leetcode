class Solution {
public:
    long long maxKelements(vector<int>& nums, int k) {
        priority_queue<int> pq(begin(nums), end(nums));
        long long score{0};
        for (int i = 0; i < k; ++i) {
            int n = pq.top();
            pq.pop();
            score += n;
            pq.emplace(ceil((double) n / 3));
        }

        return score;
    }
};

