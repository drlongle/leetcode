using namespace std;

class Solution {
public:
    bool canPlaceFlowers(vector<int>& flowerbed, int n) {
        int sz = flowerbed.size();
        for (int i = 0; i < sz && n > 0; ++i) {
            if (!flowerbed[i] &&  (i == 0 || !flowerbed[i-1]) && (i == sz - 1 || !flowerbed[i+1])) {
                --n;
                flowerbed[i] = 1;
            }
        }

        return n == 0;
    }
};

