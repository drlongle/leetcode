using namespace std;

class Solution {
public:
    int arrayPairSum(vector<int>& nums) {
        sort(begin(nums), end(nums));
        int res = 0, sz = nums.size();
        for (int i = 0; i < sz; i+= 2)
            res += nums[i];
        return res;
    }
};

