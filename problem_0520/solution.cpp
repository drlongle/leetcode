class Solution {
public:
    bool detectCapitalUse(string word) {
        int sz = word.size();
        int capitals = count_if(word.begin(), word.end(), [] (char ch) {return isupper(ch); });
        return capitals == 0 || capitals == sz || (capitals == 1 && isupper(word[0]));
    }
};

