class Solution {
public:
    int distance(vector<int>& p1, vector<int>& p2) {
        return max(abs(p1[0]-p2[0]), abs(p1[1]-p2[1]));
    }

    int minTimeToVisitAllPoints(vector<vector<int>>& points) {
        int res = 0;
        int n = points.size();
        for (int i = 1; i < n; ++i) {
            res += distance(points[i-1], points[i]);
        }
    return res;
    }
};

