using namespace std;

class Solution {
public:
    int sumOfUnique(vector<int>& nums) {
        unordered_map<int, int> cnt;
        for (int n: nums)
            ++cnt[n];
        int sum = 0;
        for (int n: nums) {
            if (cnt[n] == 1)
                sum += n;
        }
        return sum;
    }
};

